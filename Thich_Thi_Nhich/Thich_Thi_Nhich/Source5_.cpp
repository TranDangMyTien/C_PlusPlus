#include <iostream>
using namespace std;

int mainLT_1()
{
	// Truong hop dat sau
	int x = 10;
	cout << x++ << endl; // in ra 10
	cout << x << endl; // in ra 11

	// Truong hop dat truoc
	x = 10;
	cout << ++x << endl; // in ra 11
	system("pause");
	return 1;

}

